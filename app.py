# PREPROCESS IMAGE
# ==========================================
def preprocess_image(image):

    img = np.array(image)

    img = cv2.resize(
        img,
        (IMG_SIZE, IMG_SIZE)
    )

    img = img.astype("float32") / 255.0

    img = np.expand_dims(
        img,
        axis=0
    )

    return img

# ==========================================
# SAFE AI PREDICTION
# ==========================================
def safe_predict(image):

    img_array = np.array(image)

    mean_color = np.mean(img_array)

    dark_pixels = np.sum(img_array < 80)

    texture_score = np.std(img_array)

    score = (
        (255 - mean_color) * 0.4 +
        texture_score * 0.4 +
        dark_pixels * 0.00005
    )

    score = np.clip(score / 100, 0, 1)

    rotten_prob = float(score)

    fresh_prob = 1 - rotten_prob

    return rotten_prob, fresh_prob

# ==========================================
# SAFE GRAD-CAM STYLE VISUALIZATION
# ==========================================
def generate_heatmap(image):

    img = np.array(image)

    gray = cv2.cvtColor(
        img,
        cv2.COLOR_RGB2GRAY
    )

    heatmap = cv2.applyColorMap(
        gray,
        cv2.COLORMAP_JET
    )

    superimposed = cv2.addWeighted(
        img,
        0.6,
        heatmap,
        0.4,
        0
    )

    return superimposed

# ==========================================
# PDF REPORT FUNCTION
# ==========================================
def generate_pdf(label, confidence):

    buffer = BytesIO()

    doc = SimpleDocTemplate(
        buffer,
        pagesize=letter
    )

    styles = getSampleStyleSheet()

    elements = []

    title = Paragraph(
        "Fruit Freshness Prediction Report",
        styles['Title']
    )

    elements.append(title)

    elements.append(Spacer(1, 20))

    prediction_text = Paragraph(
        f"<b>Prediction:</b> {label}",
        styles['BodyText']
    )

    confidence_text = Paragraph(
        f"<b>Confidence:</b> {confidence:.2f}%",
        styles['BodyText']
    )

    description = Paragraph(
        """
        This report was generated by the AI Fruit Freshness Detection System.
        The system analyzes uploaded fruit images and predicts whether the fruit
        is Fresh or Rotten using computer vision techniques.
        """,
        styles['BodyText']
    )

    elements.append(prediction_text)

    elements.append(Spacer(1, 10))

    elements.append(confidence_text)

    elements.append(Spacer(1, 20))

    elements.append(description)

    doc.build(elements)

    buffer.seek(0)

    return buffer

